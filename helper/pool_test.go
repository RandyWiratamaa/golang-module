package helper

import (
	"fmt"
	"sync"
	"testing"
	"time"
)

func TestPool(t *testing.T) {
	pool := sync.Pool{
		New: func() interface{} {
			return "New"
		},
	}

	pool.Put("Randy")
	pool.Put("Wiratama")

	for i := 0; i < 10; i++ {
		go func() {
			data := pool.Get()
			fmt.Println(data)
			time.Sleep(1 * time.Second)
			pool.Put(data)
		}()

		time.Sleep(1 * time.Second) // disini bisa juga menggunakan wait group
		fmt.Println("Selesai")
	}
}
